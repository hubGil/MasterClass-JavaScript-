
// abs: Converte o sinal do número para positivo
Math.abs(10);
// resultado = 10
Math.abs(-10);
// resultado = 10

// ceil: Arredonda o número para cima
Math.ceil(1.1);
// resultado = 2
Math.ceil(-1.1);
// resultado = -1

// floor: Arredonda o número para baixo
Math.floor(9.9);
// resultado = 9
Math.floor(-9.9);
// resultado = -10

// round: Arredonda o número para cima se a parte decimal for de 5 a 9 e
// para baixo se for de 0 a 4
Math.round(4.5);
// resultado = 5
Math.round(-4.5);
// resultado = -4

// sign: Retorna 1 se o número for positivo e -1 se for negativo
Math.sign(5);
// resultado = 1
Math.sign(-5);
// resultado = -1

// trunc: Elimina a parte decimal do número, tornando-o um inteiro
Math.trunc(2.3);
// resultado = 2
Math.trunc(-2.3);
// resultado = -2
