// No pré incremento ele incremente e depois retornas
let result = 10;
console.log(`O valor de result é: ${result}`);
result++;
console.log(`O valor de result é: ${result}`);
++result;
console.log(`O valor de result é: ${result}`);
result--;
console.log(`O valor de result é: ${result}`);
--result;
console.log(`O valor de result é: ${result}`);
